package com.domhallan.learnspringframework;

public class MarioGame {
  public void run() {
    System.out.println("Mario is running");
  }

  public void up() {
    System.out.println("Mario is jumping");
  }

  public void down() {
    System.out.println("Mario is crouching");
  }

  public void left() {
    System.out.println("Mario is moving left");
  }

  public void right() {
    System.out.println("Mario is moving right");
  }

  public void fire() {
    System.out.println("Mario is shooting fireballs");
  }

  public void pause() {
    System.out.println("Mario is paused");
  }

  public void quit() {
    System.out.println("Mario has quit the game");
  }
}
